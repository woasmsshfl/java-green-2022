package ex05;

public class OperEx01 {
    public static void main(String[] args) {
        System.out.println(1 + 1); // 덧셈
        System.out.println(1 - 1); // 뺄셈
        System.out.println(2 * 2); // 곰셈
        System.out.println(2 / 2); // 나누기
        System.out.println(9 % 7); // 나머지

        // 더하기 (산술더하기 or 결합)
        System.out.println("1" + 1); // 연산이 불가하여 결합됨. 문자 1과 숫자 1이 나열됨.

    }
}
